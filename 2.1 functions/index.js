'use strict';
 

function describeCountry(country, population, capitalCity){
return `${country} has ${population} people and its capital city is ${capitalCity}`
}
console.log(describeCountry("Finland" , "six million" , "Helsinki"));
console.log(describeCountry("Israel" , "seven million" , "Jerusalem"));
console.log(describeCountry("England" , "ten million" , "London"));



